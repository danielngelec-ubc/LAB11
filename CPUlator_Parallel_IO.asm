.equ BASE_PTR, 0x80
.equ LEDR_OFFSET, 0x00
.equ HEX3_OFFSET, 0x20
.equ HEX5_OFFSET, 0x30
.equ SW_OFFSET,   0x40


.global _start
_start:

    lw t0, BASE_PTR(zero)
    
loop:
    lw t1, SW_OFFSET(t0)
    sw t1, LEDR_OFFSET(t0)
    sw t1, HEX3_OFFSET(t0)
    sw t1, HEX5_OFFSET(t0)

    jal x0, loop
.org BASE_PTR
BASE_ADDR:
    .word 0xFF200000
    
